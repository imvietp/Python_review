# Basic operator 

number_1 = int(input())
number_2 = int(input())

# Sum 
sum = number_1 + number_2

# Minus 
minus = number_1 - number_2

# Multiply
multiply = number_1 * number_2

# Division 
division = number_1 / number_2

# Power 
power = number_1 ** number_2


print("Sum = ", sum)
print("Minus = ", minus)
print("Multiply = ", multiply)
print("Division = ", division)
print("Power = ", power)